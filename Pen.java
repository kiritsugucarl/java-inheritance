public class Pen //the class is Pen
{
    public static void penName(String name)//calling penName
    {
        System.out.println("Pen name is : " + name);
    }

    public static void tipSize(int tip)//calling tipSize
    {
        System.out.println("Tip size is : " + tip + "mm");
    }

    public static void color(String color)//calling color
    {
        System.out.println("The ink color is : " + color);
    }

    public static void penType(String type)//calling penType
    {
        System.out.println("Pen type is : " + type);
    }
}
